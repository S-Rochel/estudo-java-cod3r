// Cap 9 Stream API - Introdução
package cap9StreamAPI;

public class cap910Introducao {
    
    /* 
        A proposta em torno da Streams API é reduzir a preocupação do desenvolvedor com a forma de implementar controle de fluxo ao lidar com coleções, deixando isso a cargo da API. A ideia é iterar sobre essas coleções de objetos e, a cada elemento, realizar alguma ação, seja ela de filtragem, mapeamento, transformação, etc. (https://www.oracle.com/br/technical-resources/articles/java-stream-api.html).
    */

    /* 
        O que é a Stream API?
        É um recurso que ajuda a manipular coleções de uma maneira simples e eficiente seguindo os princípios da programação funcional. Isso é interessante pois o controle de fluxo e loop ficam por conta  da API onde temos que nos preocupar somente com a regra do negócio.
    */
}
