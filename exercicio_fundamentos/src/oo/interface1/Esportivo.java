package oo.interface1;

public interface Esportivo {
    
    // A interface vai ter métodos e esses métodos não pode ter corpo. A classe que herdar da interface terá obrigação de implementar esses métodos.
    // Uma classe só pode herdar de uma única classe, uma classe pode herdar de multiplas interfaces.
    // Assim quando uma classe herda de uma interface ela tem a obrigação de implementar os métodos da interface.

    void ligarTurbo();
    void desligarTurbo();
}
